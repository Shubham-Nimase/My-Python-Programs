
# Sequance approach
def StartS():
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    print("Jay Ganesh")
    
# Iterative approach
def StartI():
    iCnt = 0
    while iCnt < 5:
        print("Jay Ganesh")
        iCnt = iCnt + 1
        
def main():
    print("Output by sequance")
    StartS()
    print("Output by Iteration")
    StartI()
    
if __name__ == "__main__":
    main()
